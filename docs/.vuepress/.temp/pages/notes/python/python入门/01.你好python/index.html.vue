<p>不使用解释器，计算机不认识Python代码哦</p>
<p><img src="@source/notes/python/Python入门/01.你好python/assets/image-20230810105834290.png" alt="image-20230810105834290"></p>
<p><strong>使用Python解释器程序，就能执行Python代码了</strong></p>
